package com.atin.arcface.model;

public class TemperatureError {
    private String errCode;
    private String err;
    private String temNoCorrect;
    private String envirTem;
    private String temperature;

    public TemperatureError() {
    }

    public String getErrCode() {
        return errCode;
    }

    public String getErr() {
        return err;
    }

    public String getTemNoCorrect() {
        return temNoCorrect;
    }

    public String getEnvirTem() {
        return envirTem;
    }

    public String getTemperature() {
        return temperature;
    }
}
